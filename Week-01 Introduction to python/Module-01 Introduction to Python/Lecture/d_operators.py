# arithmetic operators -> +, -, *, /, %, //
num1 = 45
num2 = 5
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(10 / 3)
print(10 % 3)
print(10 // 3)

# comparison operators -> >, <, >=, <=, ==, !==
# no: ++, -- exist in python
power = num1 ** num2
print(power)

