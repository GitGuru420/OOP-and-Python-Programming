# Convert number to string:
number = 12345
print(f"{number} and type of {number} {type(number)}")

cnvrt_str = str(number)
print(f"{cnvrt_str} and type of {cnvrt_str} {type(cnvrt_str)}")

# Convert float to number:
fnumber = 3.1416
print(f"{fnumber} and type of {fnumber} {type(fnumber)}")

cnvrt_num = int(fnumber)
print(f"{cnvrt_num} and type of {cnvrt_num} {type(cnvrt_num)}")